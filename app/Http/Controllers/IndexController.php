    {
        $input = $request->all();
        unset($input['_token']);
        $status = \DB::table('t_pengaduan')->insert($input);

        if($status){
          return redirect('/index#contact')->with('success', 'Pengaduan berhasil dikirim!');
        }else{
          return redirect('/index#contact')->with('error', 'Pengaduan gagal dikirim!');
        }
